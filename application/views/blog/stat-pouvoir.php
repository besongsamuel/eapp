								<b>Salaire moyen</b>
								
								<p>En 2016, le salaire moyen dans la RMR de Québec était de 44 404 $ (42 835 $ au Québec). Selon le Conference Board du Canada, la région a enregistré une augmentation de 1,9 % par rapport à 2015 (+2,7 % au Québec). Cette croissance permet d’inscrire une majoration moyenne de 2,9 % annuellement depuis 5 ans, surpassant celle de 2,2 % pour l’ensemble du Québec. Le besoin de main-d’œuvre persistant dans les secteurs liés à l’économie du savoir et le taux d'emploi élevé contribuent à la hausse du salaire moyen à Québec. Ces facteurs demeureront présents, au cours des cinq prochaines années, laissant entrevoir une progression annuelle moyenne autour de 2 %; une croissance similaire à celles attendues pour les autres régions métropolitaines canadiennes d’importance.</p>
								
								<b>Taux d’inflation</b>
								
								<p>Le prix du panier de biens de consommation dans la région métropolitaine de Québec connaît une hausse modérée, tout comme dans le reste du Québec. En 2016, le taux d’inflation était de 0,6 % dans la RMR comparativement à 0,7 % au Québec. La plupart des composantes ont observé des hausses, soit les biens d’alimentation, du logement et du vêtement. Certains biens importés, victimes de la dévaluation du dollar canadien, pourraient voir leur prix augmenter en 2017. Cependant, la stabilisation du côté des prix du logement pourrait faire contrepoids face à cette tendance. Dans ces circonstances, le taux d’inflation devrait, ainsi, demeurer à l’intérieur de la fourchette cible de la Banque du Canada, soit autour de 2 % par année à Québec d’ici les cinq prochaines années.</p>
								
								<b>Revenu personnel disponible</b>
								
								<p>La RMR conserve le premier rang au Québec, avec un revenu personnel disponible (RPD) par habitant de 36 128 $ (27 893 $ au Québec), une hausse de 2,3 % par rapport à 2015 (+3,8 % au Québec). Cet accroissement permet de préserver le pouvoir d’achat des ménages de la région puisqu’il est supérieur à la hausse du taux d’inflation. Qui plus est, avec une croissance moyenne de 3,3 % enregistrée entre 2011 et 2016, la région de Québec s’est hissée comme l’une des plus performantes dans l’est du pays. À long terme, l’augmentation des revenus d’emploi, des rendements boursiers et des paiements de transferts permettra à cette tendance de perdurer. En fait, les prévisions chiffrent la hausse annuelle moyenne du RPD par habitant à 2,6 % d’ici 2021 dans la région métropolitaine (+2,5 % au Québec).</p>
								
								<b>Ventes au détail</b>
								
								<p>En 2016, les ventes au détail ont atteint 14,2 G$ dans la RMR, une hausse de 1,9 % par rapport à 2015 (+4,5 % au Québec). Ce dynamisme appuie la réalisation de différents projets visant l’expansion et la modernisation des installations commerciales, mais aussi l’arrivée de nouveaux joueurs qui pourraient s’implanter à Québec. De plus, le virage numérique auquel adhèrent les détaillants assurera leur compétitivité dans un contexte où le commerce électronique prend de l’ampleur. À long terme, ces initiatives combinées à la croissance de l’emploi, de la démographie et des revenus des ménages soutiendront la pérennité du secteur. Selon le Conference Board du Canada, les ventes au détail devraient progresser en moyenne d’environ 2,3 % par année d’ici 2021, reproduisant la performance des cinq dernières années.</p>
						
