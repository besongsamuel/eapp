                            <div class="single-blog-desc">
                                <p>Faire son épicerie, ça coûte cher. Il est tellement facile d’y entrer pour quelques produits et d’y ressortir avec un portefeuille vide. Puisque nous devons tous nous nourrir, pas le choix, il vaut mieux privilégier les repas faits maison, qui coûtent moins cher et sont plus nourrissants. </p>

                                <p>Mais il arrive qu’on dépense plus qu’on le voudrait au supermarché. Il n’existe aucune recette miracle pour couper sa facture de moitié, mais avec un peu de motivation, vous pourriez faire de grandes économies à long terme..</p>

                                <blockquote>Voici 20 trucs et astuces pour acheter moins et mieux à l’épicerie :</blockquote>
								
								<ol>
									<li>Tenez-vous loin des rangées du milieu : Les épiceries placent habituellement les ingrédients essentiels, comme les produits laitiers et les fruits et légumes, de chaque côté du magasin. Ça force les clients à vérifier toutes les rangées et à acheter des articles dont ils n'ont pas vraiment besoin.</li>
									<li>N'ayez pas peur des coupons : Les coupons peuvent vous faire économiser beaucoup. Vérifiez des sites spécialisés et combinez les coupons aux ventes en vigueur en magasin. La circulaire reste le meilleur moyen de savoir ce qui est en promotion. Lisez-la chaque semaine, vous pourriez être surpris à quel point elle est utile!</li>
									<li>Laissez tomber les articles de toilette : Alors que d'acheter les articles de toilette s'avère super facile, vous payez le prix de la commodité. Gardez ces achats pour la pharmacie, où ces articles sont beaucoup moins chers.</li>
									<li>Ne vous laissez pas tenter par les marques : Vous êtes peut-être habitué à une sorte de céréale en particulier, mais les marques génériques sont moins chères, d'habitude. Elles utilisent le même nom de produit, mais avec leur propre étiquette et offrent un meilleur prix. En cas de doute, vérifiez les ingrédients.</li>
									<li>Regardez en haut et en bas, parce que les produits plus chers sont à la hauteur des yeux : Puisque nous avons tendance à regarder les articles à notre niveau, les épiceries placent les produits plus dispendieux à la hauteur des yeux. En magasinant, vérifiez les autres produits plus haut ou plus bas pour de meilleurs prix.</li>
									<li>Souvenez-vous des bons d'achats différés : Quand un produit utilisé par tous, comme l'huile d'olive, est en solde, il part rapidement. Renseignez-vous à propos des «rain checks», qui vous offrent de racheter le produit plus tard avec le même rabais auquel vous auriez eu droit ce jour-là.</li>
									<li>Considérez les haricots et fèves secs : La plupart des gens optent pour des légumineuses en boîte parce que la pensée de les faire cuire leur fait trop peur ou qu'ils pensent ne pas avoir le temps. Mais un bon chaudron de fèves est super facile à faire - promis! - et goûte encore meilleur.</li>
									<li>N'allez pas à l'épicerie le ventre vide : Plusieurs d'entre nous allons magasiner après le travail et avant le souper, au moment où nous commençons à avoir faim. Mauvaise idée! Vous aurez tendance à acheter beaucoup plus qu'à votre habitude. Essayez d'aller à l'épicerie la fin de semaine, après avoir mangé.</li>
									<li>Mangez des produits de saison : Vous l'avez déjà entendu, et vous l'entendrez encore: mangez des produits de la saison! Non seulement ils seront infiniment plus goûteux, mais cela vous fera sauver de l'argent.Restez loin des mets préparés : N'achetez pas la nourriture déjà préparée, même si elle a l'air délicieuse. Vous êtes déjà à l'épicerie, achetez les ingrédients et faites le plat voulu pour une fraction du prix.</li>
									<li>Ne vous laissez pas berner par les épices exotiques : Faire de nouvelles expériences est toujours une bonne idée, mais ce n'est pas toujours une bonne idée d'acheter ces épices à votre épicerie du coin. Allez plutôt faire un tour à un marché international, qui vous fera économiser sur les épices et des ingrédients spécifiques.</li>
									<li>Pensez-y deux fois avant d'acheter des bouteilles d'eau : L'eau embouteillée est une industrie milliardaire, et elle provient de vos poches. La bouteille ordinaire de 300 ml coûte environ un dollar, et il est conseillé d'en boire quatre par jour: ce qui équivaut à 120$ par mois pour une boisson gratuite quand elle provient de votre robinet!</li>
									<li>Prenez le temps de faire une liste d'épicerie : Vous avez peut-être une bonne mémoire, mais... écrivez votre liste d'épicerie quand même. Not seulement vous vous assurerez de ne pas oublier des produits, mais vous éviterez de faire des achats compulsifs.</li>
									<li>Faites garder vos enfants à la maison, si possible : Évidemment, ce n'est pas toujours possible de laisser bébé à la maison pendant que vous faites l'épicerie, mais si possible, allez-y lorsque vous êtes seul. Les enfants voudront parfois vous faire acheter des choses dont vous n'avez pas besoin, et ce n'est pas toujours facile de dire non.</li>
									<li>Souvenez-vous que vous payez pour de la laitue pré-lavée : Oui, c'est bien d'avoir des feuilles de laitue prêtes à manger, mais vous paierez trois fois le prix. Achetez votre propre tête de laitue, lavez-la et coupez-la tout de suite, vous ne remarquerez même pas la différence!</li>
									<li>Des fruits pré-coupés aussi, ça coûte cher : Si quelqu'un se faire payer pour faire un travail que vous pouvez facilement faire à la maison, comme couper une mangue ou un melon d'eau, vous allez payer pour le service.</li>
									<li>Ne gaspillez pas votre argent avec quelques herbes coupées : Mettez vos herbes préférées en pot pour des saveurs à portée de main en tout temps! Les herbes « fraîches » pré-coupées ne le restent pas si longtemps, après tout.</li>
									<li>Râpez votre propre fromage : Tout comme la laitue pré-lavée et les fruits pré-coupés, le fromage râpé est pratique, mais coûte plus cher.</li>
									<li>Laissez tomber les épices mélangées : Les mélanges cocasses et sauces à marinade peuvent facilement coûter 5$ ou plus à la pièce. C'est une perte d'argent incroyable, puisque vous avez la plupart des ingrédients sous la main, souvent! Souvenez-vous que la majorité des mélanges contient beaucoup de sel.</li>
									<li>20-	Comptez vos sous, même pour les patates!: Les sacs de patates s'avèrent être une véritable aubaine. Parfois, il vous coûtera autant d'argent pour deux pommes de terre individuelles qu'un sac complet. Restez à l'affût!</li>
								</ol>
