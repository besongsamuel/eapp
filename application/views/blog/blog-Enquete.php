								<blockquote>Acheter plus pour économiser plus?</blockquote>
								
								<p>Les Québécois adorent Costco : la part de marché des magasins-entrepôts et grandes surfaces, dont Costco et Walmart, est passée de 13 % en 2004 à près de 21 % en 2014, selon les plus récentes données du ministère de l’Agriculture, des Pêcheries et de l’Alimentation du Québec (MAPAQ). Or, nous n’avons pas pu inclure Costco dans notre palmarès des supermarchés parce que certains produits et plusieurs grandes marques y sont introuvables. Ainsi, nous n’y avons pas vu de bacon Maple Leaf, Lafleur ou Olymel ; seule la marque Kirkland Signature, par ailleurs appréciée de nos lecteurs (voyez l'article <a href="https://www.protegez-vous.ca/Sante-et-alimentation/supermarches/Enquete-de-satisfaction-vos-epiceries-preferees">Enquête de satisfaction: vos épiceries préférées</a> ), y était offerte. En outre, les grands formats vendus dans les entrepôts de cette chaîne sont difficiles à comparer avec ceux offerts dans les autres épiceries.</p>
								
								<blockquote>Payer pour économiser</blockquote>
								
								<p>Si on ne considère que les articles de notre liste vendus par Costco, le panier y est 13 % moins cher que notre panier moyen des marques économiques et 5 % moins cher que ce même panier au Walmart. Le hic, c’est qu’il faut être membre de Costco pour pouvoir en profiter, ce qui vous coûtera annuellement 55 $ (membre Privilège) ou 110 $ (membre Exécutif, assorti d’une remise de 2 % sur vos achats). Il faut aussi vouloir acheter en grand format et souvent en plusieurs exemplaires. Oui, votre boîte de maïs en conserve ne vous reviendra qu’à 1 $, mais vous devrez en acheter 12 pour profiter de ce prix. De plus, on trouve parfois ce maïs à 99 ¢ l’unité, et même à 88 ¢, ailleurs. Costco offre aussi peu de rabais. Parmi les articles de notre liste, seuls le jus d’orange et le savon à vaisselle étaient en solde lors de notre passage. Bref, si vous avez de la place pour stocker les produits, que vous êtes prêt à faire des compromis sur les grandes marques et que vous acceptez de vous rendre dans plus d’un établissement chaque semaine pour faire vos courses, vous pourriez économiser en allant au Costco. Mais n’y allez pas les yeux fermés.</p>

