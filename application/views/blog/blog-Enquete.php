								<p>Par Catherine Crépeau (journaliste) et Clémence Lamarche (chargée de projets) Mise en ligne : 12 septembre 2016  |  <i> Magazine : octobre 2016 </i> </p>
								
								<p>Dans quelle épicerie et dans quelle région sont les aliments les moins chers? Est-il avantageux de courir les rabais de supermarché en supermarché? Les marques maison en valent-elles toujours la peine? Voici ce que révèle notre enquête. Par un heureux hasard, IGA a lancé sa campagne de changement de prix quelques jours après le début de notre grand magasinage, ce qui nous a permis de faire d’intéressantes comparaisons. Nos premières visites ont eu lieu avant la réduction annoncée, et les autres, après. En moyenne, le prix du panier contenant des produits de marques nationales a diminué de 7 $ après l’annonce, et celui des marques économiques, de 14 $.</p>
								
								<p>Notre analyse vient toutefois préciser ce que les médias et des consommateurs ont rapidement dénoncé, en avril dernier : si certains prix ont diminué (30 sur 49 dans notre panier de marques nationales), d’autres ont augmenté (16 dans ce même panier). Trente-cinq produits du panier des marques les moins chères ont été touchés par la baisse de prix, alors que 10 autres se vendent plus cher depuis la mise en place du programme. Pour refléter au mieux la réalité actuelle, les moyennes affichées dans nos tableaux ne tiennent compte que des prix recensés après la chute de prix. Des rayons importants de l’épicerie ne sont pas touchés par le programme, soit les fruits et légumes, ainsi que les viandes et poissons. Cette dernière catégorie constitue d’ailleurs le point faible d’IGA Extra. Les filets de saumon, par exemple, y sont vendus en moyenne 32 $ le kilo, contre quelque 25 $ ailleurs. Même lorsqu’il y a des rabais, IGA Extra est moins économique qu’ailleurs : le saumon le moins cher que nous y avons vu était à 26 $ le kilo, alors que le même produit était parfois vendu sous les 20 $ le kilo dans presque tous les autres commerces.</p>
							
								<p>Pour offrir des baisses de prix dans les IGA, IGA Extra, IGA Tradition et Rachelle-Béry, Sobeys a, entre autres, renégocié avec ses fournisseurs et utilisé les économies réalisées en abandonnant le cadeau offert pour chaque tranche de 60 ou 70 $ d’achat. En tout, les prix de près de 8 500 produits auraient été réduits, ce qui représente environ 20 % des marchandises d’un supermarché, explique Marie-Noëlle Cano, directrice des communications et du développement durable chez Sobeys Québec. Mais où sont donc ces rabais ? Dans notre liste d’épicerie, ce sont les prix du sac de 2 kg de sucre blanc, de la brique de 460 g de cheddar doux et de la boîte de six barres de collation aux brisures de chocolat qui ont le plus chuté.</p>
							
								<p>À l’inverse, le prix des pommes de terre et celui des mouchoirs ont augmenté. Ce serait le cas pour environ 2 000 produits, selon Marie-Noëlle Cano. Tous ces changements ont semé la confusion chez les consommateurs, qui n’arrivaient pas toujours à distinguer les divers types d’affiches en magasin. « Entre les baisses de prix, les rabais et les promotions, il y avait effectivement beaucoup d’étiquettes sur les tablettes, mais nous trouvions important de montrer nos baisses de prix. Parce que nous avons des clients qui magasinent pour [un rabais de] moins de 10 ¢ sur un produit. Mais c’est temporaire : après une période de transition, les étiquettes de baisse de prix vont disparaître. Il y en a déjà beaucoup moins que lors du lancement », assure Marie-Noëlle Cano. En effet, lors de nos visites estivales dans les supermarchés, nos magasineurs ont remarqué une baisse importante du nombre d’affichettes chez IGA.</p>
							
								<blockquote>Moins cher à Sherbrooke ou à Gatineau ?</blockquote>
								
								<p>Notre enquête ne fera pas de jaloux, puisqu’aucune ville ne paie son épicerie systématiquement plus cher ou moins cher qu’une autre. Partout, c’est au Walmart et au Maxi & Cie que vous dépenserez le moins. Il y a toutefois quelques différences : Gatineau, par exemple, abrite le Maxi & Cie et le Walmart les plus économiques de notre évaluation, mais aussi les succursales de Provigo Le Marché et de Metro Plus les plus chères que nous ayons visitées.</p>
								
								<blockquote>Rien ne sert de courir</blockquote>
								
								<p> <a href="https://www.protegez-vous.ca/Sante-et-alimentation/supermarches/Enquete-de-satisfaction-vos-epiceries-preferees"> Les résultats de notre panel DataConso </a> montrent qu’à peine un tiers des répondants demeurent fidèles à un magasin et y achètent la majorité de leurs denrées hebdomadaires en une seule visite. Or, courir les rabais d’un endroit à l’autre n’est peut-être pas aussi avantageux qu’on pourrait le croire. Nos chiffres indiquent qu’un consommateur qui ferait le tour des supermarchés pour profiter des rabais sur tous les articles de notre panier économiserait à peine 16 $ (marques nationales) ou 10 $ (marques les moins chères) par rapport à ce qu’il paierait en un seul magasinage chez Walmart. Ce faible écart fond comme neige au soleil quand on pense au temps et au coût du carburant dépensé pour faire la tournée des épiceries !</p>
								
								<p>Si vous voulez économiser, choisissez un supermarché en fonction des rabais de la semaine et faites tous vos achats en une seule fois. Pour déterminer à quel supermarché aller, regardez les produits dont les prix varient beaucoup d’une chaîne à l’autre. Selon notre étude, le prix des oignons, des pommes de terre, du beurre d’arachide et du thon en conserve fluctue de façon notable d’un endroit ou d’une semaine à l’autre. Et comme ce sont des produits qui se gardent bien, profitez-en pour faire des réserves lorsqu’ils sont en promotion. Par contre, le prix des bananes, du cocktail de légumes et, à cause de la réglementation, du lait, varie peu d’un endroit à l’autre. Cette affiche indique-t-elle un rabais ? Non, elle annonce plutôt le nouveau prix courant du produit, une confusion que plusieurs consommateurs ont reprochée à IGA sur sa page Facebook. Durant notre enquête, Metro Plus offrait le beurre d’arachide crémeux de Kraft à 3,88 $ pour 1 kg, alors que Provigo Le Marché le vendait 8,49 $, soit plus du double ! Certains rabais valent vraiment le déplacement.</p>
								
